// Declare uma variável vazia e atribua o valor null
let vazia = null;

// Verifique se 'vazia' é igual a undefined (usando ==)
console.log("vazia == undefined:", vazia == undefined);

// Verifique se 'vazia' é estritamente igual a undefined (usando ===)
console.log("vazia === undefined:", vazia === undefined);

// Declare uma variável 'naoDefinida' sem atribuir nenhum valor
let naoDefinida;

// Verifique se 'naoDefinida' é igual a undefined (usando ==)
console.log("naoDefinida == undefined:", naoDefinida == undefined);

// Verifique se 'naoDefinida' é estritamente igual a undefined (usando ===)
console.log("naoDefinida === undefined:", naoDefinida === undefined);