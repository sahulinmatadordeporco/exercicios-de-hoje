// Declare as variáveis 'a' e 'b'
let a = 5;
let b = 10;

// Compare se 'a' é menor que 'b'
console.log("a < b:", a < b);

// Verifique se 'a' é igual a 'b' (usando ==)
console.log("a == b:", a == b);

// Verifique se 'a' é estritamente igual a 'b' (usando ===)
console.log("a === b:", a === b);

// Compare se 'a' é diferente de 'b' (usando !=)
console.log("a != b:", a != b);

// Compare se 'a' é estritamente diferente de 'b' (usando !==)
console.log("a !== b:", a !== b);